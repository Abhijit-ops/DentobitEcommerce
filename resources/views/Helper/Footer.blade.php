<script>
    $(document).ready(function () {
        cartCount();
        getCartDetails();
        $('#savetoCartButton').click(function () {
            $.ajax({
                type: 'POST',
                url: "{!! route('cart.show') !!}",
                cache: false,
                data: {_token: "{{csrf_token()}}",},
                success: function (data) {
                    $('#cartContent').html(data);

                    cartCount();
                }
            })
        });

        $(`#addToCartButton`).click(function () {
            console.log(`#addToCartButton_${$('#p_id').val()}`);
            $.ajax({
                type: 'POST',
                url: "{!! route('cart.add') !!}",
                cache: false,
                data: {
                    _token: "{{csrf_token()}}",
                    'id': $('#p_id').val(),
                    'name': $(`#name_${getId}`).val(),
                    'imgages': $(`#imgages_${getId}`).val(),
                    'price': $(`#price_${getId}`).val(),
                    'qty': $(`#qty_${getId}`).val(),
                },
                success: function (data) {
                    toastr.success('Item Added To the Cart');
                    getCartDetails();
                    cartCount();
                }
            })
        });
    });

    function updateQty(id) {
        getId = $(id).data('panel-id');
        qtyItem = $(`#qtyItem_${getId}`).val();
        $.ajax({
            type: 'POST',
            url: "{!! route('cart.update') !!}",
            cache: false,
            data: {_token: "{{csrf_token()}}", 'id': getId, 'qty': qtyItem},
            success: function (data) {
                console.log(data)
                refressCart();
                toastr.success('Cart updated');
                getCartDetails();
                cartCount();
            }
        })
    }

    function deleteCartItem(id) {
        getId = $(id).data('cart-item-id');
        $.ajax({
            type: 'POST',
            url: "{!! route('cart.remove') !!}",
            cache: false,
            data: {_token: "{{csrf_token()}}", 'id': getId},
            success: function (data) {
                refressCart();
                toastr.error('Item deleted From the Cart');
                getCartDetails();
                cartCount();
            }
        })
    }

    function cartCount() {
        $.ajax({
            type: 'POST',
            url: "{!! route('cart.count') !!}",
            cache: false,
            data: {_token: "{{csrf_token()}}",},
            success: function (data) {
                $('#countCart').html(data);
            }
        })
    }

    function getCartDetails() {
        $.ajax({
            type: 'POST',
            url: "{!! route('cart.show') !!}",
            cache: false,
            data: {_token: "{{csrf_token()}}",},
            success: function (data) {
                $('#cartContent').html(data);

            }
        });
    }

    function storeToCart(id) {
        var getId = $(id).data('panel-id');
        $.ajax({
            type: 'POST',
            url: "{!! route('cart.add') !!}",
            cache: false,
            data: {
                _token: "{{csrf_token()}}",
                'id': getId,
                'name': $(`#name_${getId}`).val(),
                'imgages': $(`#imgages_${getId}`).val(),
                'price': $(`#price_${getId}`).val(),
                'qty': $(`#qty_${getId}`).val(),
            },
            success: function (data) {
                console.log(data);
                if (data['status']) {
                    toastr.error(`Product Quantity Out Of Stock  ${data['qtyLeft']} `);
                } else {
                    toastr.success('Item Added To the Cart');
                }

                getCartDetails();
                cartCount();
            }
        })
    }

    function refressCart() {
        $('#shopping-cart-section').load(location.href + ' #shopping-cart-section');
    }

</script>
